package ch6;

// 16번+17번 연습문제

public class Printer {
	
	void printIn(int value) {
		System.out.println(value);
	}
	
	void printIn(boolean value) {
		System.out.println(value);
	}
	
	void printIn(double value) {
		System.out.println(value);
	}
	
	void printIn(String value) {
		System.out.println(value);
	}

}